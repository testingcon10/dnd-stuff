# ── File Generation ───────────────────────────────────────────────────────────

def generate_spell_file(spell):
    """Generate a single spell .md file and write it to the correct directory."""
    school = spell["school"]
    level = spell["level"]
    level_dir = LEVEL_DIRS[level]
    target_dir = os.path.join(VAULT_SPELLS, school, level_dir)
    os.makedirs(target_dir, exist_ok=True)

    filepath = os.path.join(target_dir, f"{spell['name']}.md")
    if os.path.exists(filepath):
        print(f"  SKIP (exists): {spell['name']}")
        return False

    # Build classes YAML array
    classes_yaml = "[" + ", ".join(f'"{c}"' for c in spell["classes"]) + "]"

    # Build 5e.tools link
    encoded_name = urllib.parse.quote(spell["name"].lower(), safe="'-")
    source_lower = spell["source"].lower()
    tools_url = f"https://5e.tools/spells.html#{encoded_name}_{source_lower}"

    # Build content
    lines = [
        "---",
        "tags: [spell, reference]",
        f"spell_level: {level}",
        f'school: "{school}"',
        f'casting_time: "{spell["time"]}"',
        f'range: "{spell["range"]}"',
        f'components: "{spell["comp"]}"',
        f'duration: "{spell["dur"]}"',
        f"classes: {classes_yaml}",
        f"ritual: {'true' if spell['ritual'] else 'false'}",
        f"concentration: {'true' if spell['conc'] else 'false'}",
        f'source: "{spell["source"]}"',
        "---",
        f"# {spell['name']}",
        "",
        spell["desc"],
    ]

    # Optional "At Higher Levels"
    if spell.get("higher"):
        lines.append("")
        lines.append("## At Higher Levels")
        lines.append(spell["higher"])

    # Footer
    lines.append("")
    lines.append("---")
    lines.append(f"\U0001f517 [Full Details on 5e.tools]({tools_url})")
    lines.append("")

    content = "\n".join(lines)

    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)

    return True


def main():
    print(f"Spell directory: {VAULT_SPELLS}")
    print(f"Total spells to generate: {len(SPELLS)}")
    print()

    created = 0
    skipped = 0
    for spell in SPELLS:
        if generate_spell_file(spell):
            created += 1
            print(f"  Created: {spell['name']}")
        else:
            skipped += 1

    print()
    print(f"Done! Created {created} spell files, skipped {skipped}.")


if __name__ == "__main__":
    main()
